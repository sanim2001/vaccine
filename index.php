          <h2  id="list-item-1">Earliest appointment available</h2>
          <p>Appointments can be limited, so it is in your best interest to schedule the soonest appointment that works for you. Remember though, you could have some mild side effects in the 24 hours that follow. Try not to schedule your appointment before an important exam or significant commitment.</p>
          <h2  id="list-item-2">Don’t get other vaccines at the same time</h2>
          <p>Before you get the COVID-19 vaccine, avoid getting any other vaccines for 14 days. Once you’ve had your second dose of the COVID-19 vaccine, wait 14 days to get any other vaccines. It’s more important to get the COVID-19 vaccine when it’s available. So adjust the timing of your other vaccines to make sure you get the COVID-19 vaccine as soon as possible.</p>
          <h2  id="list-item-3">Have pain relievers on hand </h2>
          <p>Ibuprofen and acetaminophen can help if you develop side effects like fever, pain or headaches. Side effects are normal and mean your immune system is responding to the vaccine. Most people experience more side effects after the second dose. Do not take pain relievers before vaccination to try to prevent vaccine-related side effects.</p>
          <h2  id="list-item-4">Grab groceries ahead of time</h2>
          <p>Get groceries before you’re scheduled to get your shot. Pick up things you’d get if you were sick, like chicken noodle soup, crackers and sports drinks. The COVID-19 vaccine will not give you COVID-19 but some people feel nausea as a side effect.</p>
          <h2  id="list-item-5">Be ready to roll</h2>
          <p>Wear a shirt with sleeves that are easy to roll up or a jacket over a short-sleeved shirt. Bonus points if you wear a favorite outfit perfect for posting your selfie on social media later.</p>
          <h2 id="list-item-6">Fuel up</h2>
          <p>Eat something and drink water the day of your vaccination. Some people get nervous when they get any kind of shot and can feel dizzy or lightheaded. Proper nutrition and hydration will combat that. Staying hydrated contributes to your overall well-being and helps your body prepare to respond to the vaccine. It is advised to refrain from drinking alcohol and intense workouts before your vaccination.</p>
